#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else
	{
		if (n < 0)
		{
			n = -n;
			write(fd, "-", 1);
		}
		n >= 10 ? ft_putnbr_fd(n / 10, fd) : 0;
		ft_putchar_fd(n % 10 + '0', fd);
	}
}
