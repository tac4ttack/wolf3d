#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int len;

	i = ft_strlen(s1);
	len = ft_strlen(s2) + 1;
	while (--len)
	{
		s1[i] = *s2++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
