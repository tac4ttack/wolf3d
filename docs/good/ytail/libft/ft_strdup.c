#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t		size;
	char		*dest;
	size_t		i;

	i = 0;
	size = ft_strlen((char *) s1);
	dest = (char *) malloc(sizeof(*s1) * size);
	if (dest == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
